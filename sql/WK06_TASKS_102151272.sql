SELECT M.MOVIENO, M.TITLE,M.RATINGCODE, R.SHORTDESC, M.TMDB_SCORE, M.RUNTIME
FROM MOVIE1272 M
INNER JOIN RATING1272 R
ON M.RATINGCODE = R.RATINGCODE
WHERE (M.RATINGCODE = 'MA' AND ((M.RUNTIME >= 100 AND M.RUNTIME <= 102)AND (M.TMDB_SCORE >= 6.5 AND M.TMDB_SCORE <= 7.5)))
OR (M.RATINGCODE = 'G' AND M.RUNTIME < 90 AND (M.TMDB_SCORE >= 6.5 AND M.TMDB_SCORE <= 7.5))
OR (M.RATINGCODE = 'PG' AND M.RUNTIME >=120 AND M.RUNTIME <= 125 AND (M.TMDB_SCORE >= 6.5 AND M.TMDB_SCORE <= 7.5))
OR (M.RATINGCODE = 'M' AND RUNTIME >= 180AND (M.TMDB_SCORE >= 6.5 AND M.TMDB_SCORE <= 7.5))
ORDER BY M.MOVIENO ASC;
